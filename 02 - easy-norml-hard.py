# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]

import math
list1 = [34, 16, 18, -5, 9, 225, 169, 27, -1 ]
new_list = []
for i in list1 :
    if (i > 0) and (math.sqrt(i) % 1 == 0) :
        new_list.append(math.sqrt(i))
        
print('Исходный список ',list1)
print('Полученный список',new_list)
print()
