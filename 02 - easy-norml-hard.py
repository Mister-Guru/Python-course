# Задание-2: Дата задана в виде строки формата 'dd.mm.yyyy'.
# Проверить, корректно ли введена дата.
# Условия корректности:
# 1. День должен приводиться к целому числу в диапазоне от 1 до 30(31)
#  (в зависимости от месяца, февраль не учитываем)
# 2. Месяц должен приводиться к целому числу в диапазоне от 1 до 12
# 3. Год должен приводиться к целому положительному числу в диапазоне от 1 до 9999
# 4. Длина исходной строки для частей должна быть в соответствии с форматом 
#  (т.е. 2 символа для дня, 2 - для месяца, 4 - для года)
print('Nard. Задача-2:')
date = input("Введите произвольную дату в формате dd.mm.yyyy - ")
date_list = date.split('.')
day = int(date_list[0])
month = int(date_list[1])
year = int(date_list[2])
month_31 = [1, 3, 5, 7, 8, 10, 12]
if len(date_list[0]) != 2 or len(date_list[1]) != 2 or len(date_list[2]) != 4:
    print('Некорректный формат! Дата должна быть в формате dd.mm.yyyy')
elif day > 31 or day < 1:
    print('Некорректный день')
elif month > 12 or month < 1:
    print('Некорректный месяц')
elif year < 1:
    print('Некорректный год')
elif month not in month_31 and day > 30:
    print('Некорректный день для этого месяца')
else:
    print('Дата введена корректно: ', date)
